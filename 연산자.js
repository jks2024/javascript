// 산술연산자 : +, -, *, /, %, **(거듭 제곱 연산자)

let x = 10;
let y = 4;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y); // 결과값이 실수이면 실수 타입으로 결정 남
console.log(parseInt(x / y));
console.log(x ** y);

// 비교연산자
let a = 10;
let b = "10";

console.log(a == b); // 동등연산자 : 두 값이 같은지를 비교
console.log(a === b); // 일치연산자 : 두 값과 데이터 타입이 같은지 비교

console.log(a != b);
console.log(a !== b);

